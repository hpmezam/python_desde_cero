# Condicional múltiple: if-elif-else
# Evaluar una nota por categoría:
# Si es 9 o más → "Excelente"
# Si está entre 7 y 8.9 → "Aprobado"
# Y si es menor que 7 → "Reprobado"

nota = 5
if nota >= 9:
    print("Excelente")
elif nota >= 7:
    print("Aprobado")
else:
    print("Reprobado")
